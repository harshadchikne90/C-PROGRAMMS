#include<stdio.h>
int main()
// format specifires
{
    int no2=23;
    char ls='P';
    float fn1=90.12;
    double dnm=123456543.98766789345;
    
    
     
    printf("value of no2=%d\n",no2); //integer format specifier---[%d]
    printf("value of ls=%c\n",ls); // charecter format specifier---[%c]
    printf("value of fn1=%f\n",fn1); // float format specifier---[%f]
    printf("value of dnm=%lf\n",dnm); // double format specifier---[%lf] 

     return 0;
}